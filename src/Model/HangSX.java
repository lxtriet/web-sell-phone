package Model;

public class HangSX {
	private String MaHSX;
    private String TenHangSX;
    
	public HangSX() {
		super();
	}

	public HangSX(String maHSX, String tenHangSX) {
		super();
		MaHSX = maHSX;
		TenHangSX = tenHangSX;
	}

	public String getMaHSX() {
		return MaHSX;
	}

	public void setMaHSX(String maHSX) {
		MaHSX = maHSX;
	}

	public String getTenHangSX() {
		return TenHangSX;
	}

	public void setTenHangSX(String tenHangSX) {
		TenHangSX = tenHangSX;
	}
   
    
}
